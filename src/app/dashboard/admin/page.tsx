  const [loading, setLoading] = useState(true);
  const [error,   setError]   = useState("");

  const fetchData = useCallback(async () => {
    setLoading(true); setError("");
    try {
      const res  = await fetch(`${API}/admin-dashboard.php`, { credentials: "include" });
      if (res.status === 401) { setError("Session expired."); return; }
      const json = await res.json();
      if (json.success) setData(json);
      else setError(json.error || "Failed to load dashboard.");
    } catch { setError("Unable to connect."); }
    finally  { setLoading(false); }
  }, []);

  useEffect(() => { fetchData(); }, [fetchData]);

  useEffect(() => {
    const id = setInterval(fetchData, 60_000);
    return () => clearInterval(id);
  }, [fetchData]);

  if (loading && !data) return (
    <div className="admin-loading"><Loader2 size={22} className="spin" /> Loading admin data...</div>
  );
  if (error) return <div className="admin-error">{error}</div>;
  if (!data) return null;

  const { kpis, roleBreakdown, recentAudit, recentAlerts, shiftSummary } = data;

